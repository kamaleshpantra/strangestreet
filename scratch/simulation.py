candidates = [(post, post.ml_score) for post in simulated_posts]

# Run the real production Bandit logic!
ranked_posts = rank_feed_with_bandit(candidates)

print(f"{'Rank'} | {'Post Content':<50} | {'ML Score':<10} | {'Final Blended Score':<15}")
