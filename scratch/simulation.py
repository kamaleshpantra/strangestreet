# Print Before State
print(f"{'Post Content':<50} | {'ML (SVD) Score'}")
print("-" * 75)
for post in sorted(simulated_posts, key=lambda x: x.ml_score, reverse=True):
    print(f"{post.content:<50} | {post.ml_score:.4f}")

